import { z } from 'zod'

/**
 * A single RAG pipeline evaluation sample.
 *
 * Represents one question-answer-context triple to be scored by the LLM judge.
 * Provide a `groundTruth` whenever you want to run the `contextRecall` metric.
 *
 * @example
 * ```typescript
 * const sample: RagSample = {
 *   id: 'q1',
 *   question: 'What is the capital of France?',
 *   answer: 'The capital of France is Paris.',
 *   contexts: ['France is a country in Western Europe. Its capital is Paris.'],
 *   groundTruth: 'Paris',
 * }
 * ```
 */
export const RagSampleSchema = z.object({
  /**
   * Optional identifier for traceability. Appears in results, JUnit classnames,
   * and SARIF location messages. Auto-assigned as "1", "2", ... if omitted.
   */
  id: z.string().optional(),
  /** The query that was posed to the RAG pipeline. Must not be empty. */
  question: z.string().min(1, 'question must not be empty'),
  /** The answer generated by the RAG pipeline under evaluation. Must not be empty. */
  answer: z.string().min(1, 'answer must not be empty'),
  /**
   * The document chunks retrieved by the RAG pipeline and passed as context to the LLM.
   * At least one chunk is required. Each chunk must be non-empty.
   * The order matters â€” earlier chunks are considered higher-ranked in precision scoring.
   */
  contexts: z
    .array(z.string().min(1, 'each context must not be empty'))
    .min(1, 'at least one context is required'),
  /**
   * The expected / reference answer for this question.
   * Required only by the `contextRecall` metric.
   * Samples without `groundTruth` are automatically skipped for `contextRecall`
   * and excluded from that metric's aggregate â€” no score distortion.
   */
  groundTruth: z
    .string()
    .min(
      1,
      'groundTruth must not be empty if provided; omit the field entirely to skip contextRecall for this sample',
    )
    .optional(),
  /**
   * Optional tenant identifier for multi-tenant SaaS evaluations.
   *
   * When set, this value is propagated to the matching {@link SampleResult} so
   * downstream consumers (dashboards, audit logs, per-tenant reports) can filter
   * or group results by tenant. rageval itself does not enforce isolation â€”
   * call `evaluate()` once per tenant if you need strict isolation, or tag
   * samples with `tenantId` and post-process when batching across tenants.
   */
  tenantId: z.string().min(1, 'tenantId must not be empty if provided').optional(),
  /**
   * Optional free-form metadata propagated to the result. Use it for trace IDs,
   * pipeline versions, A/B variant labels, or any other context you want carried
   * through to per-sample results. Values must be JSON-serialisable.
   */
  metadata: z.record(z.string(), z.unknown()).optional(),
})

export type RagSample = z.infer<typeof RagSampleSchema>

/**
 * A dataset is an ordered array of {@link RagSample} objects.
 * At least one sample is required.
 */
export const DatasetSchema = z
  .array(RagSampleSchema)
  .min(1, 'dataset must contain at least one sample')

export type Dataset = z.infer<typeof DatasetSchema>

/**
 * Supported built-in LLM provider types.
 * Use `'anthropic'` for Claude models, `'openai'` for GPT models.
 */
export const ProviderTypeSchema = z.enum(['anthropic', 'openai'])
export type ProviderType = z.infer<typeof ProviderTypeSchema>

/**
 * Names of all built-in evaluation metrics.
 *
 * | Metric | Requires groundTruth? |
 * |--------|-----------------------|
 * | faithfulness | No |
 * | contextRelevance | No |
 * | answerRelevance | No |
 * | contextRecall | **Yes** |
 * | contextPrecision | No |
 */
export const MetricNameSchema = z.enum([
  'faithfulness',
  'contextRelevance',
  'answerRelevance',
  'contextRecall',
  'contextPrecision',
])
export type MetricName = z.infer<typeof MetricNameSchema>
